#define nl "\n"
#define dq deque<int>
#define print cout <<

void solution()
{
    int n;
    cin >> n;
    vi v(n);
    for (int i = 0; i < n; i++)
    {
        cin >> v[i];
    }
    bool ans = false;
    for (int mask = 0; mask < (1 << n); mask++)
    {
        int sum = 0;
        for (int i = 0; i < n; i++)
        {
            if ((mask >> i) & 1)
            {
                sum += v[i];
            }
            else
            {
                sum -= v[i];
            }
        }
        if (sum % 360 == 0)
        {
            ans = true;
            break;
        }
    }
    if (ans)
    {
        print "YES" << nl;
        ;
    }
    else
    {
        print "NO" << nl;
        ;
    }
}

int main()
{
    fast_io;
    int t = 1;
    // cin >> t;
    while (t--)
    {
        solution();
    }
    return 0;
}
